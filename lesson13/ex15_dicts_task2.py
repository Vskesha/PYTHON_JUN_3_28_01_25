# Створіть словник, який зберігає імена студентів 
# як ключі та їхні оцінки як значення. Додайте 
# кілька студентів до словника, оновіть оцінку 
# одного з них і виведіть всі дані.

students = {
    "Alice": 88,
    "Bob": 76,
    "Carol": 92
}

# Додати нових студентів
students["David"] = 95
students["Eve"] = 82

# Оновити оцінку одного студента
students["Bob"] = 90

# Вивести всі дані
for name, grade in students.items():
    print(f"{name:<8}: {grade}")
