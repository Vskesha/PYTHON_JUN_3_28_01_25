fruits = {"apple", "banana", "cherry"}
print("Множина:", fruits)

# Метод pop()
removed_fruit = fruits.pop()
print("Видалений елемент:", removed_fruit)
print("Множина після видалення:", fruits)

# Метод clear()
numbers = set([1, 2, 3, 4, 4, 5])
print("Множина до видалення всіх елементів:", numbers)
numbers.clear()
print("Множина після видалення всіх елементів:", numbers)
