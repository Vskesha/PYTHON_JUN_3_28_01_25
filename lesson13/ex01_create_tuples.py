# Створення порожнього кортежу
empty_tuple = ()
empty_tuple = tuple()
print("Порожній кортеж:", empty_tuple)

# Створення кортежу з числами
number_tuple1 = (1, 2, 3)
number_tuple2 = tuple([8, 5, 7])
print("Кортеж з числами:", number_tuple1)
print("Кортеж з числами:", number_tuple2)

# Створення змішаного кортежу
mixed_tuple = (1, "Hello", 3.14, True)
print("Змішаний кортеж:", mixed_tuple)

# Cтворення кортежу з одним елементом
single_number_tuple1 = (3, )
integer_variable = (4 )
print(type(single_number_tuple1))
print(type(integer_variable))
