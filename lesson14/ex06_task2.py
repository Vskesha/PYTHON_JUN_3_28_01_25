# Завдання 2. Інвертування умови за допомогою not
# Використовуємо not для інвертування значення
# is_raining. Якщо не йде дощ (is_raining є False),
# то виводиться повідомлення про те,
# що парасольку можна не брати.

is_raining = False

if not is_raining:
    print("Парасольку можна не брати.")
else:
    print("Не забудь парасольку!")
