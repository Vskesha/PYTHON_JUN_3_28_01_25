"""
Завдання 2.
Напишіть програму, яка визначає, чи є даний рік високосним.
Примітка: кожен рік, який ділиться на чотири, є високосним,
за винятком років, які діляться на 100, але не діляться на 400
"""

year = int(input("Введіть рік: "))

if year % 400 == 0:
    print(f"{year} - це високосний рік")
elif year % 100 == 0:
    print(f"{year} - це не високосний рік")
elif year % 4 == 0:
    print(f"{year} - це високосний рік")
else:
    print(f"{year} - це не високосний рік")
