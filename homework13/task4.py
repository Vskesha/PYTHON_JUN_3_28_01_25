"""
Завдання 4.
Напишіть програму, яка перевіряє, чи є дане число
простим. Використовуйте цикл з оператором else,
щоб визначити, чи є число простим.
"""

num = int(input("Введи число: "))

for i in range(2, num):
    if num % i == 0:
        print(f"{num} - це не просте число")
        break
else:
    print(f"{num} - це просте число")
