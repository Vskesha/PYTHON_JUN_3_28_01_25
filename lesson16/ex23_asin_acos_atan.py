import math 

print("Арксинус 1:", math.asin(1))
print("Арксинус 0:", math.asin(0))
print("Арксинус -1:", math.asin(-1))
print("-" * 50)

print("Арккосинус 1:", math.acos(1))
print("Арккосинус 0:", math.acos(0))
print("Арккосинус -1:", math.acos(-1))
print("-" * 50)

print("Арктангенс 1:", math.atan(1))
print("Арктангенс 0:", math.atan(0))
print("Арктангенс -1:", math.atan(-1))
print("-" * 50)
