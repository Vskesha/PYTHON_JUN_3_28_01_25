import math

print("Синус кута 0 радіан:", math.sin(0))
print("Синус кута π радіан (180 градусів):", math.sin(math.pi))
print("Синус кута π/2 радіан (90 градусів):", math.sin(math.pi / 2))
print("-" * 50)

print("Косинус кута 0 радіан:", math.cos(0))
print("Косинус кута π радіан (180 градусів):", math.cos(math.pi))
print("Косинус кута π/2 радіан (90 градусів):", math.cos(math.pi / 2))
print("-" * 50)

print("Тангенс кута 0 радіан:", math.tan(0))
print("Тангенс кута π/4 радіан (180 градусів):", math.tan(math.pi / 4))
print("Тангенс кута π/2 радіан (90 градусів):", math.tan(math.pi / 2))
print("-" * 50)
